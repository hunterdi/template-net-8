// Ten, Zetsu, Ren e Hatsu.

// Ten, é a técnica de concentração e defesa. Ela é a base para todas as outras técnicas de Nen e é usada para manter a aura do praticante em uma camada protetora externa.
// A técnica envolve concentrar a aura em torno do corpo e mantê-la sob controle para que o praticante possa se proteger de ataques externos.

// Zetsu, é a técnica de supressão. Ela é usada para suprimir a aura e torná-la imperceptível para outros usuários de Nen e animais sensíveis à aura.
// É útil em situações em que um usuário de Nen deseja passar despercebido ou evitar ser detectado por outros usuários.

// Ren, é a técnica de intensificação. Ela é usada para aumentar a quantidade de aura que um usuário pode liberar e controlar.
// O treinamento de Ren é fundamental para aumentar a eficácia de habilidades de Nen que requerem uma grande quantidade de aura.

// Hatsu, é a técnica de expressão da aura. Ela é a habilidade individual de cada usuário de Nen e é usada para expressar a aura de formas únicas e personalizadas.
// A habilidade de Hatsu é determinada pelo tipo de aura do usuário e sua personalidade.